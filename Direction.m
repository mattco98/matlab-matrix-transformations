classdef Direction
    % Direction Enum that represents a directions in 3-space
    
    enumeration
        % X - The x direction
        % Represents the the unit vector [1 0 0]
        X 
        
        % Y - The y direction
        % Represents the the unit vector [0 1 0]
        Y
        
        % Z - The z direction
        % Represents the the unit vector [0 0 1]
        Z
    end
end

